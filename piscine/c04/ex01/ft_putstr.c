void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

#include <stdio.h>
#include <unistd.h>

int main(){
  char str[] = "hello";
  ft_putstr(str);
  return (0);
}
